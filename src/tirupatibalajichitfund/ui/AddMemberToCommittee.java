private void setupCommitteeCombo() {
        committeeList.addItem("None");
        try {
            ArrayList<CommitteeData> allcommitteelist = AllCommitteeClass.getInstance().getAllCommitteesActive();
            for (CommitteeData data : allcommitteelist) {
                committeeList.addItem(data.getCfid() + " " + data.getCname());
            }
        } catch (Exception ex) {
            Logger.getLogger(AddMemberToCommittee.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    private void setupEnteries() {
        enteriesCombo.addItem("None");
        for (int i = 1; i < 5; i++) {
            enteriesCombo.addItem(i + "");
        }
    }
    private void memberListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_memberListActionPerformed
        // TODO add your handling code here:
        if (memberList.equals(evt.getSource())) {
            String selMem = (String) memberList.getSelectedItem();
            if (selMem.equals("None")) {
                enteriesCombo.setEnabled(false);
                committeeList.setEnabled(false);
                addButton.setEnabled(false);
                return;
            }
            try {
                MemberData mem = AllMemberClass.getInstance().getMember(Integer.parseInt(selMem.substring(0, 4)));
                //JOptionPane.showMessageDialog(this,"Mem-"+mem.getPhone());
                // enteriesCombo.setEnabled(true);
                this.selectedMember = mem;
                committeeList.setEnabled(true);
                setupCommitteeCombo();
            } catch (Exception ex) {
                Logger.getLogger(AddMemberToCommittee.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_memberListActionPerformed

