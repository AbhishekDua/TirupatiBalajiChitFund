    private void committeeListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_committeeListActionPerformed
        // TODO add your handling code here:
        if (committeeList.equals(evt.getSource())) {
            String selCom = (String) committeeList.getSelectedItem();
            if (selCom.equals("None")) {
                enteriesCombo.setEnabled(false);
                addButton.setEnabled(false);
                return;
            }
            try {
                CommitteeData com = AllCommitteeClass.getInstance().getCommittee(Integer.parseInt(selCom.substring(0, 6)));
                JOptionPane.showMessageDialog(this, "Com-" + com.getCname());
                this.selectedCommittee = com;
                enteriesCombo.setEnabled(true);
                setupEnteries();
            } catch (Exception ex) {
                Logger.getLogger(AddMemberToCommittee.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_committeeListActionPerformed

    private void enteriesComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enteriesComboActionPerformed
        // TODO add your handling code here:
        if (enteriesCombo.equals(evt.getSource())) {
            String enteries = (String) enteriesCombo.getSelectedItem();
            if (enteries.equals("None")) {
                addButton.setEnabled(false);
                return;
            }
            int entry = Integer.parseInt(enteries);
            if (this.selectedCommittee != null) {
                int totalentry = MemberInfoClass.getInstance().getEnteriesSumForCfid(this.selectedCommittee.getCfid());
                if (totalentry == -1) {
                    JOptionPane.showMessageDialog(this, "Some error occurred while fetching total members");
                    return;
                }
                if ((totalentry + entry) > 19) {
                    JOptionPane.showMessageDialog(this, "Total Members in Committee " + (totalentry + entry) + " exceeds 19");
                    return;
                }
                
            }
            this.totalenteries = entry;
            addButton.setEnabled(true);
        }
    }//GEN-LAST:event_enteriesComboActionPerformed
    
    private void reset() {
        committeeList.setSelectedIndex(0);
        enteriesCombo.setSelectedIndex(0);
        memberList.setSelectedIndex(0);
    }
    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        if (addButton.equals(evt.getSource())) {
            if (selectedMember != null && selectedCommittee != null) {
                String refkey = MemberInfoUtility.getInstance().formRefKey(selectedMember, selectedCommittee, totalenteries);
                if (refkey == null) {
                    JOptionPane.showMessageDialog(this, "Unable to perform operation");
                    return;
                }
                ArrayList<MemberInfoData> meminfdat = MemberInfoClass.getInstance().getDatasetForCfid(selectedCommittee.getCfid());
                for (MemberInfoData mid : meminfdat) {
                    if (mid.getMember().getName().equals(selectedMember.getKName())) {
                        JOptionPane.showMessageDialog(this, "Member already in present committee");
                        return;
                    }
                }
                int ans = JOptionPane.showConfirmDialog(this, "Are you sure you want to add " + selectedMember.getName()
                        + " to committee " + selectedCommittee.getCname() + " with entries " + totalenteries);
                if (ans == JOptionPane.YES_OPTION) {
                    MemberInfoUtility.getInstance().addNewEntry(selectedMember, selectedCommittee, totalenteries, refkey);
                    reset();
                }
            }
        }
    }//GEN-LAST:event_addButtonActionPerformed

    private void resetButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetButtonActionPerformed
        // TODO add your handling code here:
        if (resetButton.equals(evt.getSource())) {
            reset();
        }
    }//GEN-LAST:event_resetButtonActionPerformed

